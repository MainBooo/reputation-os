export function classifySentiment(text: string): 'POSITIVE' | 'NEGATIVE' | 'NEUTRAL' | 'UNKNOWN' {
  const normalized = String(text || '').toLowerCase().replace(/ё/g, 'е').trim()

  const negative = [
    'плохо', 'плохой', 'плохая', 'плохое', 'плохие',
    'ужас', 'ужасно', 'ужасный', 'ужасная', 'ужасное',
    'кошмар', 'кошмарно', 'отвратительно', 'отвратительный',
    'худшее', 'худший', 'хуже', 'ниже среднего',
    'разочарован', 'разочарование', 'разочаровали',
    'не понравилось', 'не рекомендую', 'не советую',
    'никогда больше', 'больше не приду', 'не вернусь',
    'испортили', 'испорчен', 'обман', 'обманули', 'мошенники',
    'хамство', 'хам', 'грубость', 'грубый', 'нагрубили',
    'грязно', 'грязный', 'воняет', 'запах',
    'долго', 'очередь', 'ждали', 'ждать', 'медленно',
    'дорого', 'переплата', 'цены завышены',
    'невкусно', 'холодное', 'пересолено', 'сухое',
    'шумно', 'тесно', 'душно',
    'не работает', 'сломано', 'проблема', 'проблемы',
    'минус', 'минусы', 'жалоба', 'претензия',
    'bad', 'terrible', 'awful', 'worst', 'poor',
    'disappointed', 'dirty', 'rude', 'slow', 'expensive',
    'scam', 'fraud', 'never again', 'do not recommend'
  ]

  const positive = [
    'отлично', 'отличный', 'отличная', 'отличное', 'отличные',
    'хорошо', 'хороший', 'хорошая', 'хорошее', 'хорошие',
    'супер', 'класс', 'круто', 'топ', 'огонь',
    'понравилось', 'понравился', 'понравилась',
    'рекомендую', 'советую', 'вернусь', 'приду еще',
    'спасибо', 'благодарю', 'молодцы',
    'лучший', 'лучшая', 'лучшее', 'лучшие',
    'прекрасно', 'прекрасный', 'замечательно',
    'вкусно', 'уютно', 'чисто', 'быстро',
    'вежливо', 'доброжелательно', 'приятно',
    'профессионально', 'качественно',
    'доволен', 'довольна', 'довольны',
    'плюс', 'плюсы',
    'great', 'good', 'excellent', 'best', 'nice',
    'perfect', 'amazing', 'recommend', 'thanks',
    'clean', 'fast', 'friendly', 'professional'
  ]

  if (!normalized) return 'UNKNOWN'
  if (negative.some((word) => normalized.includes(word))) return 'NEGATIVE'
  if (positive.some((word) => normalized.includes(word))) return 'POSITIVE'

  return 'NEUTRAL'
}
